    private void JBCrearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JBCrearActionPerformed
        Datos d = new Datos();
        String rs = JTFRazon.getText();

        String cuit = JTFCuit.getText();
        if (!rs.isEmpty() && !cuit.isEmpty()) {
            try {
                Empresa e = new Empresa(rs, cuit);
                d.addEmpresa(e);
                JOptionPane.showMessageDialog(this, "Empresa creada con Exito");
                JCBEmpresas.addItem(rs);
                JTFRazon.setText("");
                JTFCuit.setText("");
                JTFRazon.requestFocus();
                mostrarEmpresas();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Hubo un error al crear la empresa: " + e.getMessage());
            }
        } else {
            JOptionPane.showMessageDialog(this, "Ambos campos deben estar llenos");
        }


    }//GEN-LAST:event_JBCrearActionPerformed

    private void JTFCuitKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_JTFCuitKeyReleased
        // TODO add your handling code here:
        //CREAMOS EL PATRON PARA LA EXPRESION REGULAR
        String patron = "^[0-9]{2}[-][0-9]{8}[-][0-9]$";
        //CREAR EL OBJETO QUE COMPILARA EL PATRON DE LA EXPRESION REGULAR
        Pattern patt = Pattern.compile(patron);
        //CAPTURAR EL TEXTO QUE TIENE EL JTEXTFIELD
        String texto = JTFCuit.getText();

        //CREAR EL OBJETO MATCHER QUE COMPARARA EL TEXTO CON EL PATRON GENERADO
        Matcher match = patt.matcher(texto);

        //PREGUNTAR SI HA HECHO MATCH EL TEXTO CON EL PATRON Y HACER LAS ACCIONES CORRESPONDIENTES
        if (!match.matches()) {
            JTFCuit.setBackground(Color.red);
            JBCrear.setEnabled(false);

        } else {
            JTFCuit.setBackground(null);
            JBCrear.setEnabled(true);

        }


    }//GEN-LAST:event_JTFCuitKeyReleased

    private void JBAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JBAgregarActionPerformed
        Datos d = new Datos();

        String dni = JTFDocumento.getText();
        String name = JTFNombre.getText();
        String surname = JTFApellido.getText();
        String sueldo = JTFSueldo.getText();
        String cat = JCBCategoria.getSelectedItem().toString();
        String emp = JCBEmpresas.getSelectedItem().toString();
        String[] campos = {dni, name, surname, sueldo, cat, emp};
        String sueldoPatt = "^[0-9]+([,][0-9]{2,})*$";
        Pattern patt = Pattern.compile(sueldoPatt);
        Matcher match = patt.matcher(sueldo);

        if (match.matches()) {
            if (isAllFill(campos)) {
                try {
                    Empleado trab = new Empleado(Integer.parseInt(dni), name + " " + surname, cat, Double.parseDouble(sueldo), d.buscarEmpresa(emp));
                    Empresa aux = d.buscarEmpresa(emp);
                    aux.agregarEmpleado(trab);
                    mostrarEmpleados();
                    JOptionPane.showMessageDialog(this, "Empleado Creado con exito!");
                } catch (NumberFormatException e) {
                    JOptionPane.showMessageDialog(this, "Error en Formato de dato: " + e.getMessage());
                }

            } else {
                JOptionPane.showMessageDialog(this, "Hay campos que faltan datos, no te hagas el picaro!");
            }
        }else{
            JOptionPane.showMessageDialog(this, "El formato del sueldo esta mal ");
        }


    }//GEN-LAST:event_JBAgregarActionPerformed


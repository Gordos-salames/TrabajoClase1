    public void mostrarEmpresas() {
        Datos d = new Datos();

        String[] col = {"Razon social", "Cuit"};

        DefaultTableModel tm;
        tm = new DefaultTableModel(col, 0);

        for (Empresa e : d.allEmpresas()) {
            Object[] dato = {e.getRazonSocial(), e.getCuit()};
            tm.addRow(dato);
        }

        JTEmpresas.setModel(tm);

    }

    public void mostrarEmpleados() {
        Datos d = new Datos();

        String[] col = {"Documento", "Nombre y Apellido", "Sueldo", "Categoria", "Empresa"};

        DefaultTableModel tm;
        tm = new DefaultTableModel(col, 0);

        for (Empresa e : d.allEmpresas()) {
            for (Empleado em : e.getEmpleados()) {
                Object[] dato = {em.getDni(), em.getNombreApellido(), em.getSueldo(), em.getCategoria(), e.getRazonSocial()};
                tm.addRow(dato);
            }
        }

        JTEmpleados.setModel(tm);

    }

    public boolean isAllFill(String[] campos) {
        boolean flag = true;
        for (String s : campos) {
            if (s.isEmpty() || s.equals(" ")) {
                flag = false;
                break;
            }
        }
        return flag;
    }

